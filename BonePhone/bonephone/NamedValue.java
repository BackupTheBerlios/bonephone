package bonephone;

/** Class to hold a pair of name and value. */
public class NamedValue {

	/** name of the value. */
	public String name;

	/** value for thi name. */
	public String value;

	/** Standard constructor.
	 * @param n Name.
	 * @param v Value.
	*/
	public NamedValue (String n, String v) {
		name=n; value=v;
	}
}


